/*Quando falamos em API estamos condicionados a falar de REST e RESTful
REST = Representational State Transfer, priincipios e regras que permitem
criar uma aplicação com interfaces bem definidas;
RESTful = Capacidade de aplicar os principios do REST; */

/*REST e os verbos HTTP

Sempre que criamos uma API devemos utulizar os verbos HTTP
corretos para cada URL da nossa aplicação.
Verbo GET: para solicitar dados;
Verbo POST: para inserir dados;
Verbo DELETE: para deletar dados;
Verbo: PUT: para atualizar dados;*/



/*Express

Um framework web, muito utilizada para criar aplicações em Node.js
Utiliza em seu core o módulo HTTP para criar as comunicações 
Além de outros módulos;
Podemos criar uma API com o Express facilmente.*/